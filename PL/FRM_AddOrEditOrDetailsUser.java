    void getImage(byte[] img) {
        ImageIcon imageIcon = new ImageIcon(new ImageIcon(img).getImage().getScaledInstance(lbl_img.getWidth(), lbl_img.getHeight(), Image.SCALE_SMOOTH));
        lbl_img.setIcon(imageIcon);
    }
    private void btn_imgMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_imgMouseClicked
        JFileChooser chooser = new JFileChooser();
        chooser.showOpenDialog(null);
        File f = chooser.getSelectedFile();
        fileName = f.getAbsolutePath();
        ImageIcon imageIcon = new ImageIcon(new ImageIcon(fileName).getImage().getScaledInstance(lbl_img.getWidth(), lbl_img.getHeight(), Image.SCALE_SMOOTH));
        lbl_img.setIcon(imageIcon);
        try {

            File image = new File(fileName);
            FileInputStream fis = new FileInputStream(image);
            ByteArrayOutputStream bos = new ByteArrayOutputStream();
//            byte[] buf = new byte[1024];
            byte[] buf = new byte[5120];

            for (int readNum; (readNum = fis.read(buf)) != -1;) {
                bos.write(buf, 0, readNum);
            }
            person_image = bos.toByteArray();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }//GEN-LAST:event_btn_imgMouseClicked

    private void btn_addMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_addMouseClicked
        int active;

        if (cb_active.isSelected()) {
            active = 1;
        } else {
            active = 0;
        }
        String role;
        if (cob_role.getSelectedItem().toString() == "مدير") {
            role = "مدير";
        } else {
            role = "مستخدم";
        }
        
        UserUserNameHandler uunh = new UserUserNameHandler();
        UserNameHandler unh = new UserNameHandler();
        UserPasswordHandler uph = new UserPasswordHandler();
        UserRoleHandler urh = new UserRoleHandler();
        uunh.setNext(unh);
        unh.setNext(uph);
        uph.setNext(urh);
        uunh.HandlerRequest(new User(txt_userName.getText(),
                txt_name.getText(), txt_password.getText(),
                role, person_image, active));

        if (uunh.check&&unh.check&&uph.check&&urh.check) {

            if (btn_add.getText().toString() == "إضافة") {
                if (cls.checkUserName(txt_userName.getText())) {
                    JOptionPane.showMessageDialog(null, "اسم المستخدم موجود بالفعل");
                } else {

                    cls.addOne(txt_userName.getText(), txt_name.getText(), txt_password.getText(), role, person_image, active);
                    up.countRows();
                    up.showData();
                    JOptionPane.showMessageDialog(null, "تمت عملية الإضافة بنجاح");
                }

            } else if (btn_add.getText().toString() == "تعديل") {
                if (cls.checkUserName(txt_userName.getText()) && !txt_userName.getText().equals(userName)) {
                    JOptionPane.showMessageDialog(null, "اسم المستخدم موجود بالفعل");
                } else {
                    cls.updateOne(txt_userName.getText(), txt_name.getText(), txt_password.getText(), role, person_image, active, userName);
                    up.showData();
                   JOptionPane.showMessageDialog(null, "تمت عملية التعديل بنجاح");
                }
            }

        }

    }//GEN-LAST:event_btn_addMouseClicked

