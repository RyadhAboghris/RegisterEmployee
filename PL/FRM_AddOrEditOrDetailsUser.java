    public void editOperation(String un) {

        this.userName = un;
        add = false;
        btn_add.setText("تعديل");
        User user = cls.getOne(userName);
        txt_userName.setText(user.getUserName());
        txt_name.setText(user.getName());
        txt_password.setText(user.getPassword());
        if (user.getImage() != null) {
            getImage(user.getImage());
            person_image=user.getImage();
        }
        if (user.getActive() == 1) {
            cb_active.setSelected(true);
        } else {
            cb_active.setSelected(false);
        }
        if (user.getRole().contains("مدير")) {
            cob_role.setSelectedIndex(0);
        } else {
            cob_role.setSelectedIndex(1);
        }

    }

